#include <iostream>
#include <algorithm>
#include <cstring>
using namespace std;

#define ll long long

const ll N = 1e5+7, I = 1e14+7;
int n, f[N<<1]; 
ll k, a[N], h[N<<1];

/**
 * fenwick tree
 * vnoi implementation
 * 
 * see:
 * https://vnoi.info/wiki/algo/data-structures/fenwick.md
 * */
void update(int i) {
	i++;
	while (i <= (n<<1|1)) {
		f[i]++;
		i += i&-i;
	}
}

int get(int i) {
	i++;
	int s = 0;
	while (i > 0) {
		s += f[i];
		i -= i&-i;
	}
	return s;
}

/**
 * 
 * */
ll cnt(ll m) {
	// gotta cite the black magic
	// first time using memset
	memset(f, 0, N<<1<<2);
	int hlen = n<<1|1;
	for (int i = 0; i < n; i++) {
		h[i<<1]   = a[i+1];
		h[i<<1|1] = a[i+1] - m;
	}
	h[n<<1] = 0;
	sort(h, h+hlen);
	update(lower_bound(h, h+hlen, 0) - h);
	//
	ll res = 0;
	for (int i = 1; i <= n; i++) {
		res += get(lower_bound(h, h+hlen, a[i] - m) - h);
		update(lower_bound(h, h+hlen, a[i]) - h);
	}
	return res;
}

ll calc() {
	ll l = -I, r = I;
	while (l < r) {
		ll m = (l + r + 1) >> 1;
		if (cnt(m) >= k) {
			l = m;
		} else {
			r = m - 1;
		}
	}
	return l;
}

